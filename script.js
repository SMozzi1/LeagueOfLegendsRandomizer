function detectorDeOpcion() {
    const element = document.querySelector(".roles");
    let value = element.value;
    return value;
}

function randomizarPersonaje() {
    const linea = detectorDeOpcion();
    let randomChamp = "";
    let result = "";
    let imgSrc = "";
    
    switch (linea) {
      case "top":
        randomChamp = Math.floor(Math.random() * toplaneChampions.length);
        result = toplaneChampions[randomChamp];
        imgSrc = toplaneSquares[randomChamp];
        break;
      case "jg":
        randomChamp = Math.floor(Math.random() * jungleChampions.length);
        result = jungleChampions[randomChamp];
        imgSrc = jungleSquares[randomChamp];
        break;
      case "mid":
        randomChamp = Math.floor(Math.random() * midlaneChampions.length);
        result = midlaneChampions[randomChamp];
        imgSrc = midlaneSquares[randomChamp];
        break;
      case "adc":
        randomChamp = Math.floor(Math.random() * adcChampions.length);
        result = adcChampions[randomChamp];
        imgSrc = adcSquares[randomChamp];
        break;
      case "supp":
        randomChamp = Math.floor(Math.random() * supportChampions.length);
        result = supportChampions[randomChamp];
        imgSrc = supportSquares[randomChamp];
        break;
    }
  
    return { champion: result, imageSrc: imgSrc };
  }
  
  document.querySelector("#randomicer").addEventListener("click", function() {
    const { champion, imageSrc } = randomizarPersonaje();
    document.querySelector("#result").textContent = champion;
    document.querySelector("#championSquare").src = imageSrc;
  });
